package org.kie.benchmark.cep.wikimedia.model;

/**
 * Final alert output generated by the rule engine.
 * Represents detected anomalies, patterns, or events of interest.
 */
public class Alert {
    private String alertType;
    private int severity;
    private String user;
    private String title;
    private String details;
    private long timestamp;

    public Alert() {
    }

    public Alert(String alertType, int severity, String user, String title, String details, long timestamp) {
        this.alertType = alertType;
        this.severity = severity;
        this.user = user;
        this.title = title;
        this.details = details;
        this.timestamp = timestamp;
    }

    public String getAlertType() {
        return alertType;
    }

    public void setAlertType(String alertType) {
        this.alertType = alertType;
    }

    public int getSeverity() {
        return severity;
    }

    public void setSeverity(int severity) {
        this.severity = severity;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }

    @Override
    public String toString() {
        return "Alert{" +
                "alertType='" + alertType + '\'' +
                ", severity=" + severity +
                ", user='" + user + '\'' +
                ", title='" + title + '\'' +
                ", details='" + details + '\'' +
                ", timestamp=" + timestamp +
                '}';
    }
}
